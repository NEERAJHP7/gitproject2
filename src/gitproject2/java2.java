package gitproject2;

public class java2 {
	
	   public static void main(String[] args) {
	        String str = "Neeraj";
	        String reversed = "";

	        for (int i = str.length() - 1; i >= 0; i--) {
	            reversed = reversed + str.charAt(i);
	            System.out.println("Reversed String: " + reversed);
	            
	        }

	        System.out.println("Reversed String: " + reversed);
	       
	    }

}
